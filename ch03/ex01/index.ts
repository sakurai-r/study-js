/**
 * Infinity + Infinity = Infinity
 * Infinity - Infinity = NaN
 * Infinity * Infinity = Infinity
 * Infinity / Infinity = NaN
 */
console.log(`Infinity + Infinity = ${Infinity + Infinity}`);
console.log(`Infinity - Infinity = ${Infinity - Infinity}`);
console.log(`Infinity * Infinity = ${Infinity * Infinity}`);
console.log(`Infinity / Infinity = ${Infinity / Infinity}`);

/**
 * -Infinity + (-Infinity) = -Infinity
 * -Infinity - (-Infinity) = NaN
 * -Infinity * (-Infinity) = Infinity
 * -Infinity / (-Infinity) = NaN
 */
console.log(`-Infinity + (-Infinity) = ${-Infinity + -Infinity}`);
console.log(`-Infinity - (-Infinity) = ${-Infinity - -Infinity}`);
console.log(`-Infinity * (-Infinity) = ${-Infinity * -Infinity}`);
console.log(`-Infinity / (-Infinity) = ${-Infinity / -Infinity}`);

/**
 * NaN + NaN = NaN
 * NaN - NaN = NaN
 * NaN * NaN = NaN
 * NaN / NaN = NaN
 */
console.log(`NaN + NaN = ${NaN + NaN}`);
console.log(`NaN - NaN = ${NaN - NaN}`);
console.log(`NaN * NaN = ${NaN * NaN}`);
console.log(`NaN / NaN = ${NaN / NaN}`);
